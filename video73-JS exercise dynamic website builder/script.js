function createCard(titlevalue, cNamevalue, viewsvalue, monthsOldvalue, durationvalue, thumbnailvalue){

let card = document.createElement('div');
document.querySelector(".container").append(card)
card.className = "card";
let thumbnail = document.createElement('img')
thumbnail.setAttribute("src",thumbnailvalue)
card.append(thumbnail)
thumbnail.className = "thumbnail-img"
let textSection = document.createElement('div')
card.append(textSection)
textSection.className = 'card-text-section'

let title = document.createElement('h3')
textSection.append(title)
title.className = "title"
title.innerText = titlevalue

let list = document.createElement("ul")
list.classList = "list-view"
textSection.append(list)

let cName = document.createElement('li')
// cName.className = "cName"
list.append(cName)
cName.classList = "cName"
cName.innerText = cNamevalue
let views = document.createElement('li')
views.classList = "views"
list.append(views)
views.innerText = `${viewsCalc(viewsvalue)} views`

let monthsOld = document.createElement("li")
list.append(monthsOld)
monthsOld.classList = "months-old"
monthsOld.innerText = `${monthsOldvalue} months ago`

let duration = document.createElement("div")
duration.classList = "duration-box"
card.append(duration)
let durationText = document.createElement("p")
duration.append(durationText)
durationText.classList = "duration-text"
durationText.innerText = durationvalue



}
function viewsCalc(views){
    let ret
    let calc
    if(views>= 1000 && views <1000000){
        calc =views / 1000 
        ret = `${calc}K`
    }
    else if (views>=1000000){
        calc =views / 1000000
        ret = `${calc}M` 
    }
    return ret
}

